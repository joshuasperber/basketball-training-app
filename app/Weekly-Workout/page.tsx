function encodeAutoWorkoutSuggestion(suggestion: SuggestedWorkout) {
  const payload = {
    title: suggestion.title,
    sport: suggestion.sport,
    subcategory: suggestion.subcategory,
    notes: suggestion.notes,
    exerciseIds: suggestion.exerciseIds ?? [],
    exercises: suggestion.exercises ?? [],
  };

  try {
    return encodeURIComponent(JSON.stringify(payload));
  } catch {
    return null;
  }
}

function roundUpToNearestFive(value: number) {
  return Math.ceil(value / 5) * 5;
}

function normalizeExerciseFamily(name: string) {
  return name
    .toLowerCase()
    .replace(/\s*-\s*(rechts|links|right|left)\b/g, "")
    .replace(/\s*[-–]?\s*\d+\s*$/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function buildRelatedExerciseNames(
  workout: Workout,
  exercises: Exercise[],
  exercisesById: Record<string, Exercise>,
) {
  const baseExercises = workout.exerciseIds
    .map((exerciseId) => exercisesById[exerciseId])
    .filter((exercise): exercise is Exercise => Boolean(exercise));
  const familyKeys = new Set(baseExercises.map((exercise) => normalizeExerciseFamily(exercise.name)));
  const related = exercises.filter(
    (exercise) =>
      exercise.category === workout.category &&
      exercise.subcategory === workout.subcategory &&
      familyKeys.has(normalizeExerciseFamily(exercise.name)),
  );
  const mergedNames = [...baseExercises.map((exercise) => exercise.name), ...related.map((exercise) => exercise.name)];
  return [...new Set(mergedNames)];
}

function getSeedIndex(seedSource: string, length: number) {
  if (length <= 0) return 0;
  let hash = 0;
  for (let i = 0; i < seedSource.length; i += 1) {
    hash = (hash * 31 + seedSource.charCodeAt(i)) >>> 0;
  }
  return hash % length;
}

function computeWorkoutDuration(workout: Workout, exercisesById: Record<string, Exercise>) {
  const rawDuration = workout.exerciseIds.reduce((sum, exerciseId) => {
    const exercise = exercisesById[exerciseId];
    return sum + (exercise?.durationMin ?? 10);
  }, 0);

  return roundUpToNearestFive(rawDuration * 1.1);
}

function buildFallbackSuggestion(mode: string, minutes: number): SuggestedWorkout | null {
  if (mode === "none" || mode === "recovery" || minutes <= 0) {
    return {
      title: "Freier Tag",
      durationMin: 0,
      notes: "Heute ist ein freier Tag. Versuche trotzdem eine kleine aktive Gewohnheit zu schaffen 💪",
      sport: "-",
      subcategory: "Frei",
    };
  }

  if (mode === "game") {
    return {
      title: "Spieltag",
      durationMin: minutes,
      notes: "Nur Match-Fokus, kein zusätzliches Workout.",
      sport: "Basketball",
      subcategory: "Game",
    };
  }

  return null;
}

