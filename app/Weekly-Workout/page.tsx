function syncNoTimeForDate(dateKey: string) {
  const dailyRaw = window.localStorage.getItem("bt.daily-plan.v1");
  const dailyPlan = dailyRaw ? (JSON.parse(dailyRaw) as Record<string, PlannedWorkoutTag[]>) : {};
  dailyPlan[dateKey] = [];
  window.localStorage.setItem("bt.daily-plan.v1", JSON.stringify(dailyPlan));

  const parsedDate = new Date(`${dateKey}T00:00:00`);
  const dayMap: Record<number, DayKey> = {
    0: "sunday",
    1: "monday",
    2: "tuesday",
    3: "wednesday",
    4: "thursday",
    5: "friday",
    6: "saturday",
  };
  const targetDay = dayMap[parsedDate.getDay()];
  const rawProfile = window.localStorage.getItem("profile_cache_v4");
  if (!rawProfile) return;
  try {
    const parsed = JSON.parse(rawProfile) as { weekConfig?: Partial<Record<DayKey, { mode: string; minutes: number }>> };
        parsed.weekConfig = {
      ...(parsed.weekConfig ?? {}),
      [targetDay]: { mode: "unavailable", minutes: 0 },
    };
    window.localStorage.setItem("profile_cache_v4", JSON.stringify(parsed));
  } catch {
    // noop
  }
}

function isTodayOrFutureDate(dateKey: string) {
  const todayKey = toLocalDateKey(new Date());
  return dateKey >= todayKey;
}

function selectBestWorkout(
  mode: string,
  day: DayKey,
  targetMinutes: number,
  workouts: Workout[],
  exercises: Exercise[],
  exercisesById: Record<string, Exercise>,
  freshnessMapByCategory: Record<Category, Record<string, number>>,
  usedWorkoutIds: Set<string>,
  weeklySubcategoryUsage: Record<Category, Record<string, number>>,
  avoidSubcategories: Partial<Record<Category, string[]>>,
  position: string,
  playStyle: string,
  lastAssignedSubcategoryByCategory: Partial<Record<Category, string>>,
): SuggestedWorkout {
  const targetWithExtra = targetMinutes;
  const modeLabel = mode === "game-training" ? "Spieltraining (15 Min vorab + 30 Min danach)" : "Direktes Training";
  if (mode === "recovery") {
    return buildRecoverySuggestion(day, exercises);
  }
  const fallback = buildFallbackSuggestion(mode, targetWithExtra);
  if (fallback) return fallback;

  const desiredCategory: Category =
    mode === "gym" ? "Gym" : "Basketball";
  const focusProfile = getFocusProfile({ position, playStyle });

  const categoryFreshness = freshnessMapByCategory[desiredCategory] ?? {};
  const subcategoryRank = (subcategory: string) => categoryFreshness[subcategory] ?? 0;
  const weeklyUsage = weeklySubcategoryUsage[desiredCategory] ?? {};
  const subcategoryWeeklyCount = (subcategory: string) => weeklyUsage[subcategory] ?? 0;

  const filteredByCategory = workouts.filter((workout) => workout.category === desiredCategory);
  const availableSubcategories = [
    ...new Set(
      filteredByCategory.map((workout) => workout.subcategory),
    ),
  ];
  const preferredSubcategory = pickSubcategoryForDay({
    category: desiredCategory,
    focusProfile,
    usage: weeklySubcategoryUsage[desiredCategory] ?? {},
    availableSubcategories,
  });
  if (filteredByCategory.length === 0) {
    const generated = buildGeneratedWorkout({
      day,
      category: desiredCategory,
      subcategory: desiredCategory,
      targetMinutes: roundUpToNearestFive(targetWithExtra),
      exercisePool: exercises,
    });
    return {
      title: generated.name,
      durationMin: generated.durationMin,
      notes: generated.notes,
      sport: generated.category,
      subcategory: generated.subcategory,
      exerciseIds: generated.exerciseIds,
      exercises: generated.exerciseNames,
      isAutoGenerated: true,
    };
  }

  const filtered = filteredByCategory
    .map((workout) => ({
      workout,
      duration: computeWorkoutDuration(workout, exercisesById),
    }))
    .sort((left, right) => {
      if (left.workout.subcategory === preferredSubcategory && right.workout.subcategory !== preferredSubcategory) {
        return -1;
      }
      if (right.workout.subcategory === preferredSubcategory && left.workout.subcategory !== preferredSubcategory) {
        return 1;
      }
      return 0;
    });
  const unusedPool = filtered.filter((entry) => !usedWorkoutIds.has(entry.workout.id));
  const avoidForCategory = avoidSubcategories[desiredCategory] ?? [];
  const withoutAvoidedSubcategories = unusedPool.filter(
    (entry) => !avoidForCategory.includes(entry.workout.subcategory),
  );
  const pool =
    withoutAvoidedSubcategories.length > 0
      ? withoutAvoidedSubcategories
      : unusedPool.length > 0
        ? unusedPool
        : [];
  const nonConsecutivePool = pool.filter(
    (entry) => entry.workout.subcategory !== lastAssignedSubcategoryByCategory[desiredCategory],
  );
  const dayPool = nonConsecutivePool.length > 0 ? nonConsecutivePool : pool;

  if (dayPool.length === 0) {
    const generated = buildGeneratedWorkout({
      day,
      category: desiredCategory,
      subcategory: preferredSubcategory,
      targetMinutes: targetWithExtra,
      exercisePool: exercises,
    });
    weeklySubcategoryUsage[desiredCategory] = {
      ...weeklySubcategoryUsage[desiredCategory],
      [generated.subcategory]:
        (weeklySubcategoryUsage[desiredCategory]?.[generated.subcategory] ?? 0) + 1,
    };
    return {
      title: generated.name,
      durationMin: generated.durationMin,
      notes: `${modeLabel}: ${generated.notes} (keine duplizierten Workouts in dieser Woche).`,
      sport: generated.category,
      subcategory: generated.subcategory,
      exerciseIds: generated.exerciseIds,
      exercises: generated.exerciseNames,
      isAutoGenerated: true,
    };
  }

  const compliantPool = dayPool.filter(
    (entry) =>
      (weeklySubcategoryUsage[desiredCategory]?.[entry.workout.subcategory] ?? 0) <
      (desiredCategory === "Gym" ? 2 : 2),
  );
  const effectivePool = compliantPool.length > 0 ? compliantPool : dayPool;

  const sortedPool = [...effectivePool].sort((a, b) => {
    const weeklyDiff = subcategoryWeeklyCount(a.workout.subcategory) - subcategoryWeeklyCount(b.workout.subcategory);
    if (weeklyDiff !== 0) return weeklyDiff;

    const freshnessDiff = subcategoryRank(a.workout.subcategory) - subcategoryRank(b.workout.subcategory);
    if (freshnessDiff !== 0) return freshnessDiff;

    const durationDiff = Math.abs(a.duration - targetWithExtra) - Math.abs(b.duration - targetWithExtra);
    if (durationDiff !== 0) return durationDiff;

    const aSeed = getSeedIndex(`${day}-${a.workout.id}`, 97);
    const bSeed = getSeedIndex(`${day}-${b.workout.id}`, 97);
    return aSeed - bSeed;
  });
  const best = sortedPool[0];

  const shouldGenerateWorkout =
    best.workout.subcategory !== preferredSubcategory &&
    (weeklySubcategoryUsage[desiredCategory]?.[preferredSubcategory] ?? 0) <
      (desiredCategory === "Gym" ? 2 : 2);

  if (shouldGenerateWorkout) {
    const generated = buildGeneratedWorkout({
      day,
      category: desiredCategory,
      subcategory: preferredSubcategory,
      targetMinutes: targetWithExtra,
      exercisePool: exercises,
    });
    weeklySubcategoryUsage[desiredCategory] = {
      ...weeklySubcategoryUsage[desiredCategory],
      [generated.subcategory]:
        (weeklySubcategoryUsage[desiredCategory]?.[generated.subcategory] ?? 0) + 1,
    };
    return {
      title: generated.name,
      durationMin: generated.durationMin,
      notes: `${modeLabel}: ${generated.notes} Fokus via Spielertyp-Mapping.`,
      sport: generated.category,
      subcategory: generated.subcategory,
      exerciseIds: generated.exerciseIds,
      exercises: generated.exerciseNames,
      isAutoGenerated: true,
    };
  }

  usedWorkoutIds.add(best.workout.id);
  weeklySubcategoryUsage[desiredCategory] = {
    ...weeklySubcategoryUsage[desiredCategory],
    [best.workout.subcategory]: subcategoryWeeklyCount(best.workout.subcategory) + 1,
  };

  return {
    workoutId: best.workout.id,
    title: best.workout.name,
    durationMin: best.duration,
    notes: `${modeLabel}: Match zu ${targetWithExtra} Min (${best.workout.category} • ${best.workout.subcategory})`,
    sport: best.workout.category,
    subcategory: best.workout.subcategory,
    exerciseIds: best.workout.exerciseIds,
    exercises: buildRelatedExerciseNames(best.workout, exercises, exercisesById),
  };
}

